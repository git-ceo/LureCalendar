def fetch_diaoyu_jia(category_url: str, max_items: int = 5) -> list:
    """从钓鱼之家攻略列表抓取摘要（best-effort，失败返回空）。"""
    items = []
    try:
        resp = requests.get(category_url, headers=HEADERS, timeout=10)
        if resp.status_code != 200:
            return items
        soup = BeautifulSoup(resp.text, "html.parser")
        # 钓鱼之家的列表页有多种结构，做泛化处理
        for a in soup.select("a")[:max_items * 4]:
            href = a.get("href", "")
            text = a.get_text(strip=True)
            if not href or not text or len(text) < 8 or len(text) > 50:
                continue
            if "lure" in href.lower() or "luya" in href.lower() or "路亚" in text:
                items.append({"title": text, "url": urljoin(category_url, href)})
                if len(items) >= max_items:
                    break
    except Exception as exc:
        print(f"  ! 钓鱼之家抓取失败：{exc}")
    return items


def upsert_guide(db, data: dict) -> bool:
    existing = db.query(FishingGuideModel).filter(FishingGuideModel.title == data["title"]).first()
    now = int(time.time() * 1000)
    payload = dict(
        title=data["title"], category=data["category"],
        target_species=data.get("target_species"), season=data.get("season"),
        water_type=data.get("water_type"), summary=data.get("summary"),
        content=data.get("content"), cover_url=data.get("cover_url"),
        tags=data.get("tags"), source=data.get("source", "LureCalendar 内容团队"),
        source_url=data.get("source_url"),
        update_time=now,
    )
    if existing:
        for k, v in payload.items():
            if v is not None:
                setattr(existing, k, v)
        return False
    payload["create_time"] = now
    db.add(FishingGuideModel(**payload))
    return True


def run():
    db = SessionLocal()
    inserted = updated = 0
    try:
        # 内置高质量教程入库
        for entry in BUILTIN_GUIDES:
            print(f"→ 入库 {entry['title']}")
            new_record = upsert_guide(db, entry)
            if new_record:
                inserted += 1
            else:
                updated += 1
        db.commit()

        # 尝试网络补充（失败不影响）
        try:
            ext = fetch_diaoyu_jia("https://www.diaoyu.com/luya/", max_items=5)
            for it in ext:
                stub = {
                    "title": f"{it['title']}", "category": "钓法", "target_species": "通用",
                    "season": "全年", "water_type": "通用",
                    "summary": "来自钓鱼之家社区的精选攻略。",
                    "content": f"原文链接：{it['url']}\n\n（请在客户端中点击外链阅读完整内容）",
                    "tags": "社区,钓鱼之家", "source": "钓鱼之家", "source_url": it["url"],
                }
                if upsert_guide(db, stub):
                    inserted += 1
            db.commit()
        except Exception as exc:
            print(f"  ! 网络补充失败：{exc}")

        print(f"\n完成：新增 {inserted} 篇，更新 {updated} 篇。")
    except Exception as exc:
        db.rollback()
        print(f"教程入库失败：{exc}")
        raise
    finally:
        db.close()


if __name__ == "__main__":
    run()
